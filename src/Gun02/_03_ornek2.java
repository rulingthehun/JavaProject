package Gun02;

public class _03_ornek2 {
    public static void main(String[] args) {
        System.out.println("Merhaba Dunya"); //ln var //ln: Line = satir
        System.out.println(); //1 tane bos satir acar
        System.out.print("Javaya hosgeldiniz"); //ln yok
        System.out.println("Adimiz Java"); //ln var
        //ln: yazdiktan sonra alt satira gec
        // print.java -> Java derliyor (ara kod .class) -> JVM (exe, isletim sistemine)
        System.out.println("Merhaba \nDunya"); // \n bir alt satira gec
        //Pazartesi kelimesinin her harflerini ayri bir satira yaz.
        System.out.println("P\nA\nZ\nA\nR\nT\nE\nS\nI");
        System.out.println();
        System.out.println("s");
        System.out.println("a");
        System.out.println("l");
        System.out.println("i");
    }
}