package Gun08;

public class _05_JavaAssignmentOperators {
    public static void main(String[] args) {
        int a=10;
        a=a+5;//a'nin degerini 5 artirir.
        a+=5;//a'nin artis degeri 5, yukaridakiyle ayni.

        a=a-5;//a'nin degerini 5 azaltir.
        a-=5;//a'nin azalma degeri 5, yukaridakiyle ayni.

        a=a*5;//a'nin degerini 5 ile carpar.
        a*=5;//a'nin carpani 5'tir, yukaridakiyle ayni.

        a=a/5;//a'nin degerini 5'e bol.
        a/=5;//a'nin bolen degeri 5'tir, yukaridakiyle ayni.
    }
}
