package Gun03;

public class _06_JavaString {
    public static void main(String[] args) {
        String ad="ismet"; //String: Kelimeleri tutmak icin kullanilir, sinirlari cift tirnak("") ile belirlenir.
        String soyad="temur";
        System.out.println("ad = " + ad);
        System.out.println("soyad = " + soyad);
        System.out.println("ad ve soyad = " + ad+" "+soyad);
        String tamAd= "ismet"+" "+"temur";
        String tamAd2= ad+" "+soyad;
        System.out.println("tamAd = " + tamAd);
        System.out.println("tamAd2 = " + tamAd2);
    }
}
